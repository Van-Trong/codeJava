     private void doFindByName() {
        
        String name;
        name =txtFindName.getText();
        ArrayList<Motocycles> tempMotocycles = this.myMoto.getMotocyclesbyName(name);

        if (tempMotocycles.size() != 0) {
             MotocyclesTableModel motoTblModel = new MotocyclesTableModel(tempMotocycles);
             this.TableShow.setModel(motoTblModel);
             
             
        } else {
            JOptionPane.showMessageDialog(this,"Motocycles not found");
            //System.out.println("Motocycles not found");
        }
    }
