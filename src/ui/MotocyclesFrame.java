public void AddRowTable(){
        int row =this.TableShow.getSelectedRow();
            AddMototcyclesJDialog add = new AddMototcyclesJDialog(this.myMoto,this,"",true);
            add.setVisible(true);
       
}
public void EditRowTable(){
        int row =this.TableShow.getSelectedRow();
   
        MotocyclesTableModel model = (MotocyclesTableModel) this.TableShow.getModel();

        Motocycles selectedMoto = (Motocycles) model.getObjectAtRow(row);
    EditMototcyclesJDialog edit = new EditMototcyclesJDialog(selectedMoto,this.myMoto,this,"",true);
    edit.setVisible(true);
}
private void DeleteRowTable() {
        int row = this.TableShow.getSelectedRow();
        MotocyclesTableModel model = (MotocyclesTableModel) this.TableShow.getModel();

        Motocycles selectedMoto = (Motocycles) model.getObjectAtRow(row);
        this.myMoto.deleteMotocyclesbyID(selectedMoto.getID());
        RefreshTable();
    }
private void BackupData() {
      
        int num = this.myMoto.saveByteData();
        JOptionPane.showMessageDialog(this, "Data is backed up, there are " + num + " cd");

    }

    private void RestoreData() {
     
        int num = this.myMoto.getByteData();
        JOptionPane.showMessageDialog(this, "Data is restored , there are " + num + " cd");
        RefreshTable();
    } 

